# Tipo de dado bool (boolean)
# Ao questionar algo em um programa,
# Só exestem duas respostass posséveis:
# Sim (True) ou não (False).
# Existem eles o ==, que é um operador lógico que
# questiona se um valor é igual a outro
print(10 == 10) #Sim => True (Verdadeiro)
print(10 == 11) #Nãp => False (Falso)
print(type(True))
print(type(False))
print(type(10 == 10))
print(type(10 == 11))